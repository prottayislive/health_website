<p>
Wellness is a modern word with ancient roots. The key tenets of wellness as both preventive 
and holistic can be traced back to ancient civilizations from the East (India, China) to 
the West (Greece, Rome). In 19th-century Europe and the United States, a variety of 
intellectual, religious and medical movements developed in parallel with conventional 
medicine. With their focus on holistic and natural approaches, self-healing and preventive 
care, these movements have provided a firm foundation for wellness today.
</p>
<P>
Wellness is often confused with terms such as health, wellbeing and happiness. While there
 are common elements among them, wellness is distinguished by not referring to a static 
 state of being (i.e., being happy, in good health, or a state of wellbeing). Rather, 
 wellness is associated with an active process of being aware and making choices that lead
  toward an outcome of optimal holistic health and wellbeing.

LIFE (Living It Fully Everyday) website will manage stress, and lead the users down the path
 of becoming a more mindful and productive individuals.
